import time
name = ['Сергій', "Юля", "Максим", "Надія"]
n=[4,3,2,1]
for i in name:
    print("Зовнішній почався, а тепер переходжу до внутрішнього")
    for j in n:
        print(f"Привіт {i}, це внутрішній цикл -{j}")
        time.sleep(0.5)
    print("Внутрішній цикл закінчивя, переходжу до зовнішнього")
    time.sleep(2)
    print()